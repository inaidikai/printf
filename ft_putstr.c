#include "ft_printf.h"

int	ft_putstr(char *s)
{
	int	i;
	int	c;

	i = 0;
	c = 0;
	if (!s)
	{
		s = "(null)";
	}
	while (s[i])
	{
		c += ft_putchar(s[i]);
		i++;
	}
	return (c);
}

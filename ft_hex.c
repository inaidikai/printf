#include "ft_printf.h"

int	ft_hex(void *nb)
{
	int	y;

	y = 0;
	y += write(1, "0x", 2);
	if (nb == 0)
		y += write(1, "0", 1);
	else
		y += ft_hexa((unsigned long long)nb, 'x');
	return (y);
}
